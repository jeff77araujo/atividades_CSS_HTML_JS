/*
Faça um programa que escreva na tela os números pares entre 0 e 50, usando um
comando For. Não utilize nenhum IF/THEN neste programa, apenas o comando For-Do.
*/
//______________________________________________FINALIZADO_______________________________________________

var numero = 50
for (var contador = 0; contador <= numero; contador = contador + 2) {
    console.log(contador)
}

